package com.java.course.primitivedatatypes;

public class LogicalOperatorsExample {

	public static void main(String[] args) {

		int i = 5;
		// i>=15 && i<=25 - false

		// && Operator - Both the conditions must be true - true
		// true && true => true
		// true && false => false
		// false && true => false
		// false && false => false

		// || Operator - Atleast one condition is true - true

		// true || true => true
		// true || false => true
		// false || true => false
		// false || false => false
	}
}
